function generateChallengeDays(title: string, dayTitles: string[]): ChallengeDay[] {
  const types: ChallengeDay["type"][] = ["task", "reflection", "meditation", "exercise", "reading", "gratitude"]
  return dayTitles.map((dayTitle, i) => ({
    day: i + 1,
    title: dayTitle,
    description: `Day ${i + 1} of ${title}: Take time to practice and reflect on "${dayTitle}". Set aside dedicated time for this exercise.`,
    type: types[i % types.length],
    duration: i % 2 === 0 ? "15 min" : "10 min",
  }))
}
